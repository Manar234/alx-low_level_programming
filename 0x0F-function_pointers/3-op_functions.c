#include "calc.h"
/**
 * op_add - add two integers
 * @a: interger
 * @b: integer
 * Return: the addtion of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two integers
 * @a: integer
 * @b: integer
 * Return: the difference bettwen two intgers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product two integers
 * @a: integer
 * @b: integer
 * Return: the product of two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - devisie two integers
 * @a: integer
 * @b: integer
 * Return: the devision of two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - to have the remainder of the division of two integers
 * @a: integer
 * @b: integer
 * Return: the remainder of the devision
 */
int op_mod(int a, int b)
{
	return (a % b);
}
